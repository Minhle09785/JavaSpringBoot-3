  public  boolean check_Form() {
      if(txtMaTC.getText().equals("")){
          txtMaTC.setBackground(Color.YELLOW);
          txtMaTC.setForeground(Color.BLUE);
          txtMaTC.requestFocus();
          MsgBox.alert(this, "Bạn vui lòng nhập mã thú cưng!");
          return false;
      }else{
          txtMaTC.setBackground(Color.white);
      }
      if (txtMaTC.getText().length() > 0 && txtMaTC.getText().length() < 6) {
            MsgBox.alert(this, "Mã Thú cưng phải nhập ít nhất 6 ký tự");
            txtMaTC.setBackground(Color.yellow);
            txtMaTC.setForeground(Color.BLUE);
            return false;
        }else{
            txtMaTC.setBackground(Color.white);
        }
      if(TXTHoTen.getText().equals("")){
          TXTHoTen.setBackground(Color.YELLOW);
          TXTHoTen.setForeground(Color.BLUE);
          TXTHoTen.requestFocus();
          MsgBox.alert(this, "Bạn vui lòng nhập Tên thú cưng!");
          return false;
      }else{
          TXTHoTen.setBackground(Color.white);
      }
      if(txtSDT.getText().equals("")){
          txtSDT.setBackground(Color.YELLOW);
          txtSDT.setForeground(Color.BLUE);
          txtSDT.requestFocus();
          MsgBox.alert(this, "Bạn vui lòng nhập Số Điện Thoại!");
          return false;
      }else{
          txtSDT.setBackground(Color.white);
      }
      if (!txtSDT.getText().matches("[0-9]+")) {
            MsgBox.alert(this, "Số điện thoại chỉ nhập số");
            return false;
        }
        if (txtSDT.getText().length() < 10 || txtSDT.getText().length() > 12) {
            MsgBox.alert(this, "Số điện thoại phải nhập đủ 10 hoặc 11 số !");
            txtSDT.setBackground(Color.YELLOW);
            txtSDT.setForeground(Color.BLUE);
            return false;
        } else{
            txtSDT.setBackground(Color.white);
        }
      if(txtNgaySinh.getText().equals("")){
          txtNgaySinh.setBackground(Color.YELLOW);
          txtNgaySinh.setForeground(Color.BLUE);
          txtNgaySinh.requestFocus();
          MsgBox.alert(this, "Bạn vui lòng nhập Ngày sinh! ");
          return false;
      }else{
          txtNgaySinh.setBackground(Color.white);
      }
      if ((txtNgaySinh.getText().length() != 10)) {
            txtNgaySinh.requestFocus();
            txtNgaySinh.setBackground(Color.yellow);
            txtNgaySinh.setForeground(Color.BLUE);
                MsgBox.alert(this, "Định dạng ngày nhập vào chưa chính xác!");
                return false;
            }else{
            txtNgaySinh.setBackground(Color.white);
        }
      if(txtBenh.getText().equals("")){
          txtBenh.setBackground(Color.YELLOW);
          txtBenh.setForeground(Color.BLUE);
          txtBenh.requestFocus();
          MsgBox.alert(this, "Bạn vui lòng nhập Bệnh! ");
          return false;
      }else{
          txtBenh.setBackground(Color.white);
      }
      if(txtGhiChu.getText().equals("")){
          txtGhiChu.setBackground(Color.YELLOW);
          txtGhiChu.setForeground(Color.BLUE);
          txtGhiChu.requestFocus();
          MsgBox.alert(this, "Bạn vui lòng nhập Ghi chú! ");
          return false;
      }else{
          txtGhiChu.setBackground(Color.white);
      }
      if (lblHinhAnh.getIcon() == null) {
            MsgBox.alert(this, "Bạn chưa chọn hình! "+"\n*click vào khu vực hình để chọn!");
            
            return false;
        }
      
      
       
      return true;
    }

    private void btnExcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcelActionPerformed
        try {
            JFileChooser jFileChooser = new JFileChooser("D:\\HK4\\DuAnMau_1\\DuAnMau_QLPhongKhamThuCung1\\FileExcel");
            jFileChooser.showSaveDialog(this);
            File saveFile = jFileChooser.getSelectedFile();
            if (saveFile != null) {
                saveFile = new File(saveFile.toString() + ".xlsx");
                FileOutputStream out;
                try ( XSSFWorkbook wb = new XSSFWorkbook()) {
                    org.apache.poi.ss.usermodel.Sheet sheet = wb.createSheet("Quản Lý Thông tin thú cưng");
                    Row rowcol = sheet.createRow(0);
                    for (int i = 0; i < tblThuCung.getColumnCount(); i++) {
                        Cell cell = rowcol.createCell(i);
                        cell.setCellValue(tblThuCung.getColumnName(i));
                    }
                    for (int j = 0; j < tblThuCung.getRowCount(); j++) {
                        Row row = sheet.createRow(j + 1);
                        for (int k = 0; k < tblThuCung.getColumnCount(); k++) {
                            Cell cell = row.createCell(k);
                            if (tblThuCung.getValueAt(j, k) != null) {
                                cell.setCellValue(tblThuCung.getValueAt(j, k).toString());
                            }
                        }
                    }
                    out = new FileOutputStream(new File(saveFile.toString()));
                    wb.write(out);
                }
                out.close();
                MsgBox.alert(null, "print thành công!");
                //  openFile(saveFile.toString());
            } else {
                MsgBox.alert(null, "print thất bại!");
            }
        } catch (HeadlessException e) {
        } catch (FileNotFoundException ex) {
            Logger.getLogger(QuanLyThuCung.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(QuanLyThuCung.class.getName()).log(Level.SEVERE, null, ex);
        }

//       
                       
    }//GEN-LAST:event_btnExcelActionPerformed


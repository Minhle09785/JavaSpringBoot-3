    void fillTable() {//Đỗ dữ liệu vào bảng tblHocViẹn
 row = tblThuCung.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tblThuCung.getModel();
        model.setRowCount(0);// xóa tất cả các hàng trên Jtable
        try {

            List<ThuCung> list = dao.selectAll();// đọc cơ sở dữ liệu//selectALL:duyệt toàn bộ nhân viên

            for (ThuCung tc : list) {
                Object[] row = {
                    tc.getMaTC(),
                    tc.getTenTC(),
                    tc.getGioiTinh() ? "Đực" : "Cái",
                    tc.getSDT(),
                    XDate.toString(tc.getNgaySinh()),
                    tc.getBenh(),
                    tc.getHinh(),
                    tc.getGhiChu(),};

                model.addRow(row);// Thêm một hàng vào Jtable
            }

        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

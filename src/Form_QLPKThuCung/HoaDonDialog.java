    void setForm(HoaDonPK HDPK) {
        
        lblSoHoaDon.setText(HDPK.getMaHD());
        txtLapNgay.setText(XDate.toString(HDPK.getNgayHD()));
        txtMaNV.setText(HDPK.getMaNV());
        txtMaTC.setText(HDPK.getMaTC());
        txtthanhtien.setText(String.valueOf(String.format("%.0f",HDPK.getDonGia())));
    }

    HoaDonPK getForm() {
        // 
        HoaDonPK HDPK = new HoaDonPK();
        HDPK.setMaHD(lblSoHoaDon.getText());
        HDPK.setNgayHD(XDate.toDate(txtLapNgay.getText()));
        HDPK.setMaNV(txtMaNV.getText());
        HDPK.setMaTC(txtMaTC.getText());
        HDPK.setDonGia(Double.valueOf(txtthanhtien.getText()));
        return HDPK;

    }
        void edit() {
        String MaHD = (String) tblBangHoaDon.getValueAt(this.row, 0);
        HoaDonPK HDPK = dao.selectById(MaHD);
        this.setForm(HDPK);
        this.updateStatus();
      
    }

    void first() {
        this.row = 0;
        MsgBox.alert(this, "Đang ở đầu !");
        this.edit();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tblBangHoaDon.getRowCount() - 1) {
            this.row++;
            this.edit();
            
        }
        else if(this.row < tblBangHoaDon.getRowCount() - 1){
            this.row++;
            this.edit();
        }
    }

    void last() {
        this.row = tblBangHoaDon.getRowCount() - 1;
        this.edit();
        MsgBox.alert(this, "Đang ở cuối !");
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblBangHoaDon.getRowCount() - 1);
        // Trạng thái form
        txtMaTC.setEditable(!edit);
        txtMaNV.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        // Trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    void insert() {
        HoaDonPK HDPK = getForm();
        try {
            dao.insert(HDPK);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm mới thành công");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
        }
    }

    void update() {
        HoaDonPK HDPK = getForm();
        try {
            dao.update(HDPK);
            this.fillTable();
            MsgBox.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại!");
        }
    }

    void delete() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Bạn không quyền xoá Thú cưng");
        } else if (MsgBox.confirm(this, "Bạn thực sự muốn xoá Thú cưng này?")) {
            try {
                String maHD = lblSoHoaDon.getText();
                dao.delete(maHD);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Xoá thành công");
            } catch (Exception e) {
                MsgBox.alert(this, "Xoá thất bại");
            }

        }// 
    }

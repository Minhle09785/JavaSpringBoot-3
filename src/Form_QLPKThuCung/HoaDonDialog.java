   void fillTable() {//Đỗ dữ liệu vào bảng tblHocViẹn

        DefaultTableModel model = (DefaultTableModel) tblBangHoaDon.getModel();
        model.setRowCount(0);// xóa tất cả các hàng trên Jtable
        try {

            List<HoaDonPK> list = dao.selectAll();// đọc cơ sở dữ liệu//selectALL:duyệt toàn bộ nhân viên

            for (HoaDonPK HDPK : list) {
                Object[] row = {
                    HDPK.getMaHD(),
                    XDate.toString(HDPK.getNgayHD(), "dd/MM/yyyy"),
                    HDPK.getMaNV(),
                    HDPK.getMaTC(),
                    HDPK.getDonGia(),};
                model.addRow(row);// Thêm một hàng vào Jtable
            }

        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
   

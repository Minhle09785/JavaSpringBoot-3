    public boolean Check_Form(){
        if(txtLapNgay.getText().equals("")){
            txtLapNgay.requestFocus();
            MsgBox.alert(this, "Bạn vui lòng nhập Ngày lập!");
            txtLapNgay.setBackground(Color.YELLOW);
            return false;
        }else{
            txtLapNgay.setBackground(Color.white);
        }
        if ((txtLapNgay.getText().length() != 10)) {
            txtLapNgay.requestFocus();
            txtLapNgay.setBackground(Color.yellow);
                MsgBox.alert(this, "Định dạng ngày nhập vào chưa chính xác!");
                return false;
            }else{
            txtLapNgay.setBackground(Color.white);
        }
        if(txtMaNV.getText().equals("")){
            txtMaNV.requestFocus();
            MsgBox.alert(this, "Bạn vui lòng nhập Mã nhân viên!");
            txtMaNV.setBackground(Color.YELLOW);
            return false;
        }else{
            txtMaNV.setBackground(Color.white);
        }
        if(txtMaTC.getText().equals("")){
            txtMaTC.requestFocus();
            MsgBox.alert(this, "Bạn vui lòng nhập Mã thú cưng!");
            txtMaTC.setBackground(Color.YELLOW);
            return false;
        }else{
            txtMaTC.setBackground(Color.white);
        }
         
        if(txtthanhtien.getText().equals("")){
            txtthanhtien.requestFocus();
            MsgBox.alert(this, "Bạn vui lòng nhập số Tiền!");
            txtthanhtien.setBackground(Color.YELLOW);
            return false;
        }else{
            txtthanhtien.setBackground(Color.white);
        }
          try {
            double Tien = Double.parseDouble(txtthanhtien.getText());
        } catch (NumberFormatException e) {
           MsgBox.alert(this, "Thành tiền Phải Là Số !!!");
            txtthanhtien.setBackground(Color.yellow);
            txtthanhtien.setText("");
            txtthanhtien.requestFocus();
            return false;
        }
        
        return true;
    }
    

    void insert() {
         NhanVien nh = getForm();
        if (nh == null) {
            return;
        }
        try {
            dao.insert(nh);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm mới thành công");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
        }
    }

    void update() {
        NhanVien nh = getForm();
        if (nh == null) {
            return;
        }
       // NhanVien nh = getForm();
        try {
            dao.update(nh);
            this.fillTable();

            MsgBox.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại!");
        }
    }

    void delete() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Bạn không quyền xoá Nhân viên!");
        } else if (MsgBox.confirm(this, "Bạn thực sự muốn xoá Nhân Viên này?")) {
            try {
                String maTC = txtMaNV.getText();
                dao.delete(maTC);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Xoá thành công");
            } catch (Exception e) {
                MsgBox.alert(this, "Xoá thất bại");
            }

        }// 
    }


    public String matKhauSao(String pass) {
        String sao = "";
        for (int i = 0; i < pass.length(); i++) {
            sao += "*";
        }
        return sao;
    }

    void fillTable() {//Đỗ dữ liệu vào bảng tblHocViẹn

        DefaultTableModel model = (DefaultTableModel) tblnhanVien.getModel();
        model.setRowCount(0);// xóa tất cả các hàng trên Jtable
        try {

            List<NhanVien> list = dao.selectAll();// đọc cơ sở dữ liệu//selectALL:duyệt toàn bộ nhân viên

            for (NhanVien nv : list) {
                Object[] row = {
                    nv.getMaNV(),
                    nv.getHoTen(),
                    nv.getSDT(),
                    nv.getDiaChi(),
                    nv.getEmail(),
                    nv.getGioiTinh() ? "Nam" : "Nữ",
                    XDate.toString(nv.getNgaySinh()),
                    Auth.user.getVaiTro() ? nv.getMatKhau() : matKhauSao(nv.getMatKhau()),
                    nv.getVaiTro() ? "Bác sĩ" : "Nhân viên",
                    nv.getLuong(),
                    nv.getHinh(),
                    nv.getGhiChu(),};

                model.addRow(row);// Thêm một hàng vào Jtable
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }

    }

